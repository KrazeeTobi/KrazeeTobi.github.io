<a name=delete>Why Was My Post Deleted?</a>
</table></div>

<div class=itemtext>
So you make a post and next thing you know - it vanishes! 

<p><li>We wont delete a valid post thats in the wrong area unless its just spam or flames. ^_^ Maybe one of the admins felt that your post was better suited in another forum and it was just moved. Before you scream bloody murder, if there was another dominant topic, check to see if it was moved somewhere. Old super large posts are often retired to the archives or off topics moved to their respective forum.

<p><li>If not then you probably posted spam OR broke one of the community rules and one of the admins deleted it. Check the rules. Did you break one? Did your post fall under anything in the Spam List? Heck - it could even have been a complete accident and yours got deleted when it wasnt meant to be. Its happened before. :P<p>
