<a name=rule1>1. Flaming</a>
</table></div>

<div class=itemtext>
Flames are not tolerated. 

<p><li>Feel free to debate, but don't let the debate devolve into mindless flame wars. 

<p><li>Obviously, bigotry is not allowed.  This includes using "gay" or similar terms to express disapproval of something.

<p><li>When you do debate, try to use solid arguments instead of just acting as if it's true only because you said it or because you heard someone else say it. "I HATE AMY SHE'S STUPID!" is not an argument no matter what spin you put on it. :P However "I hate Amy because... and this is proven in these examples..." is a valid argument. See the difference? "Sonic is cool" or "Archie sucks" is opinion, but it's not really discussion material. Post WHY you think something, and be ready to discuss or prod discussion on it. 

<p><li>_DO NOT_ post a flame and then afterwards say "Oh I was just kidding!". That's annoying. Do it and try and see if you can kid yourself out of a warning. 

<p><li>If a moderater or administrator feels that your post has gotten out of hand and could possibly start a flame or a flame war, he or she has every right to delete or close it.

<p><li>Be open minded - a lot of people, from a lot of cultures, believe or do different things or have thier own quirks. You wouldnt expect someone from Britain to behave or understand some the cultures and quirks of all Americans (and vice versa). Dont be arrogant and think that your way is the only way that exists. If you have a question about other cultures or quirks from someone, feel free to ask it but dont belittle because of it. You may even learn something about others since a broad range of people visit here from Malaysia, Sweden, Australia, Great Britain, Canada, America, and many other countries. Be tolerant - someones quirk that you dont like, may often be someone elses normal way of living and doing things. If that thing offends you, don't ram it down thier throat or attack that person but try and talk to them. Maybe that person can see your way of thinking or in turn try to compromise, so that way you both can agree. Be respectful - Respect others and thier beliefs. You'll get a lot more respect in return.

<p><li>Lastly, we encourage intellectual, cool, or funny discussion on all boards. Feel free to debate others ideas and opinions if you disagree--but not the people expressing them. If you don't like someone: take it to email. You can disagree with someone's opinion without resorting to petty insults to make your point. Our forum is not a place for you to let loose grudges or point out what you don't like about someone else.<p>
</div><div class=titlebar><table class=glow>
<a name=rule2>2. Cursing & Sexually Explicit Material</a>
</table></div>

<div class=itemtext>
Severe cursing, lewd or pornographic material, and rude innuendo are not allowed.

<p><li>Though the majority of the forumers can be teens, remember to keep it at a G or PG rating. Whether we see them or not, kids post here and if thier parents are doing thier jobs like they're supposed to, then they dont want thier kid reading that stuff.  Besides, most of the older posters don't want to read it either!

<p><li>Mild cursing is tolerated, but dont curse like a drunken sailor and NOT expect to be told to tone it down.  The EZBoard cursing censors are on, but they dont delete @$$, dam*, or hell, because whether you think it vulgar or not, some people DO wish to talk without being censored and you have to compromise between the teens, kids, and adults in a place like this.

<p>No one wants to hear you graphically describe your sexual fantasies or something weird like that. Keep it to yourself. Thats just nasty and intolerable. >:P The characters are NOT REAL and its also very rude and disrepsectful to others on the board who have to read that kind of stuff when looking for a valid post.<p>
</div><div class=titlebar><table class=glow>
<a name=rule3>3. Thread Space</a>
</table></div>

<div class=itemtext>
It doesnt matter how long a thread is, but what does matter is the main page only holds 25 threads before its moved to the next. Don't start a new thread addressed to only one person, unless it's something everyone can respond to or unless its urgently drastic. If you're going to be making a lot similar-subject posts (such as questions, a "quote or post of the week," or episodes of a story you're writing on the forum), make a favorite or bookmark of that thread and post the other posts as responses to it. This will bring it to the top of the forum automatically. Besides increasing the response you get, this makes it easier to archive the forum, and to find your similar posts. :)<p>
</div><div class=titlebar><table class=glow>
<a name=rule4>4. Be Patient In Responses</a>
</table></div>

<div class=itemtext>
Just because someone doesnt answer your post or if its skipped over doesnt always mean anyone didnt read it. We get a high volume of traffic and sometimes they can be moved down the board. Sometimes people may not reply because, quite frankly, they dont have anything to say. :P If you post frequently, or try to talk with the people and/or be friendly, the local forumers may be more responsive in replying to you. As a bit of advice - try posting topics that others are interested in, dont post things that have already been said somewhere else, or try to make good conversation in already existing topics. Dont be afraid to say "I disagree" or add something new to the conversation. BUT DO NOT clutter the board with "WHY ISNT ANYONE RESPONDING TO ME YOU ALL DONT LIKE ME!!!!" and DO NOT overreact by saying you'll gouge your eyeballs out or slit your wrists, because nine times out of ten, stuff like that only makes people NOT want to respond and makes them more annoyed.<p>
</div><div class=titlebar><table class=glow>
<a name=rule5>5. What Is Considered Spam?</a>
</table></div>

<div class=itemtext>
NOTE - These rules don't apply to the spam forum.

<p>Spam is anything that "clutters" the board needlessly and is either WAAAY off topic for that area or is just insanely vapid with no reason for it to have even been posted. For instance, there's the good spam (authorized by the SPA ^__^) that can make you laugh out of your seat or bad spam that's just mad stupid and is posted for the simple fact of cluttering the vicinity. TOO MUCH of that bad spam can get on everyones nerves. :P 

<p>Posters who consistantly post spam will get warned. We're not being anal, but here are some things that we and other posters consider "bad spam". Try to avoid it when posting.

<p><li><b>Single-Sentence Topics</b> - Avoid posting one-liner topics. Sometimes such posts are good for responses, but don't make one-liner responses frequently. If your sig is large, uncheck it (there's a checkbox under the posting form) when you do post a one-liner response.  Note - this is less important in replies.  Replies can be one-liners, but don't post a TON of one-liner replies.

<p><li><b>Pointless Posts</b> - This doesn't mean you can't post wackiness. By all means do. But your posts should have some entertaining, informational, or intellectual point. "Do You like Sonic/Mega Man!" Well DUH! This is a Sonic/Mega Man messageboard. Messages with nothing more than "Sonic is cool! I like his spikes!" or just plain "Wow" are really fruitless topic wise.

<p><li><b>Insanity Vs. Spam</b> - On some of the forums, people like to get crazy and post insanity.  This is a lot of fun, but not when people post spam too.  Sometimes it's hard to tell the difference, but generally, good insanity is creative and written either to encourage replies or to have some kind of comedic value.  Filling a topic with baby talk or words related to biological functions is not comedy.  Something that involves some creativity or effort like a script or joke is good insanity.

<p><li><b>Post Counts</b> - Don't start new threads about your post counts, unless it's a 1000-mark.  Otherwise, just note it in a normal post you're making or post it in a topic set up specifically for post counts (found in the MoFo 2).  Don't pad your post count by spamming.

<p><li><b>Try To Stay On Topic For The Board You're On</b> - Look at the board you're at. If you're posting on the Segasonic board, we dont need to know everything about the characters in Archie Sonic. Or if you're at Knothole Village, you're certainly not going to talk about STC or vice versa. There's a board SOLELY for site ads, a board specifically for testing your signatures, a Guestbook Extension and boards designed with specific topics in mind where you may get a better response. The Mobius Forum is our large general anything goes area.

<p><li><b>Please Use Sigs, Pics, And Icons Properly</b> - You can have a fairly large sig (we're more lenient about this than a lot of boards), but if you use a large sig, please turn it off when making small posts.  Also, make sure sigs, pics, and icons load easily - don't use pics that are over 50K, for example.  :P  Sigs should not stretch the screen at 800x600, side pics should be 120 pixels wide or less, and icons should be 20 pixels high or less.  Also, all sigs, pics, and icons should be appropriate for this board - don't use profanity, bigotry, etc.  If you normally use a sig like that elsewhere and don't want to change it, just uncheck the "use signature" box on the posting page.

<p><li><b>PLEASE DO NOT ABUSE THE POLLS !!!!</b> - Vapid polls along the lines of "SONIC IS BLUE! DO YOU LIKE IT! YESNO!" or "Why do you not like me?" will be deleted on contact. :P Don't post a new poll everyday with nothing on it but "Are my shoes green? YES NO!" or "Do you like Segasonic? YESNO!" Remember that half of the things you say can be said without polling every minute thing in creation. ^_^ Try to use them if you want a general opinion on what a broad range of people think on a certain subject, otherwise dont bog down the board with them. And before you poll - try and see if anyone else has asked that question previously. That way you can save space. :) 

<p><li><b>DO NOT ABUSE THE SMILIES!</b> - Smilies rock ^__^ A million smilies that wont load do not. :) Use them to your hearts content - thats what they're there for, but don't get obnoxious with them. :D Don't place so many smilies in a post that you cant load it. :P And by that I mean like 50 lines worth of the same grinning circle. :D

<p><li><b>Post Something Worth Discussing!</b> - Overall, remember to use common sense when you post. :) This may sound brash, but if you're going to post at all, post something that worth discussing and not stupid spam. Try to keep your topics interesting and something that everyone can participate in. Encourage discussion rather than discourage it.<p>
</div><div class=titlebar><table class=glow>
<a name=AdminsMods>Admins & Mods</a>
</table></div>

<div class=itemtext>
The admins and mods aren't here to be bullies or dictators, but rather try to keep everything in line and make sure the board doesnt fall headfirst into the flaming pits of internet hell. ^__^ If you disagree with something an administrator has done then try and talk to the admin and figure out if you two can solve the problem peacefully so you both can avoid it again - in other words dont come barging in like a ninny demanding someones head just 'cause your toes got stepped on :). That way you can avoid confrontation and everyone can be happy. ALL Admins reserve the right to delete or edit what they deem fit, HOWEVER no admin is allowed to abuse thier status over others.

<p>Mods can be board-wide mods or forum specific, or both.  A board mod is supposed to help in moderating in general.  Meanwhile, forum-specific mods have the job of concentrating on a specific forum not just to moderate it, but also to encourage discussion there by posting themselves.

